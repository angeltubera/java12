package Basic_Structure_and_Syntax;

public class Mydatatypes {
    public static void main(String[] args) {
        System.out.println("MYDATATYPES");
        byte myNum = 100;
        System.out.println("thebyte variable: " + myNum);
        short myShortNum = 5000;
        System.out.println("the short variable: " + myShortNum);
        int myIntNum = 100000;
        System.out.println("the int variable: " + myIntNum);
        long myLongNum = 1000000L;
        System.out.println("the long variable: " + myLongNum);
        float myFloatNum = 5.75f;
        System.out.println("the float variable: " + myFloatNum);
        double myDoubleNum = 19.99d;
        System.out.println("the double variable: " + myDoubleNum);
        char myChar = 'A';
        System.out.println("the char variable: " + myChar);
        boolean myBool = true;
        System.out.println("the boolean variable: " + myBool);
        String myString = "Hello, World!";
        System.out.println("the String variable: " + myString);
        char mychar = 'A';
        System.out.println("the char variable: " + mychar);
        char myVar1 = 65, myVar2 = 66, myVar3 = 67;
        System.out.println("the char variables: " + myVar1 + ", " + myVar2 + ", " + myVar3);
        String mynull = null;
        System.out.println("the null variable: " + mynull);


    }
    
}
