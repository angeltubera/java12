package Basic_Structure_and_Syntax;

public class variables {
    public static void main(String[] args) {
      String name = "angel tubera";
      int age = 18;//
      final int score = 10;
      String subject = "Mathematics"; // declaring variables with initialization
      System.out.println("Name:" + name);
      System.out.println("age:" + age);
      System.out.println ("Score:" + score);
      System.out.println("Subject:" + subject);
      age = 18; //reassigning a new value to the variable age
      // score = 20; // cannot reassign a final variable
      System.out.println("New age:" + age);
      Float height = 5.8f; // declaring a float variable
      System.out.println("Height:" + height);
      char section = 'A'; // declaring a char variable
      System.out.println("Section:" + section);
      boolean isStudent = true; // declaring a boolean variable
      System.out.println("Is Student:" + isStudent);
      


      
        
    }
}

