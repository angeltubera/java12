package Basic_Structure_and_Syntax;

public class another {
    public static void main(String[] args) {
        System.out.println("This is another class.");
        String name = "eme lang";
        System.out.println("Name: " + name);
        System.out.println("This class is used to demonstrate that we can have multiple classes in the same package.");
        int age = 25;
        System.out.println("Age: " + age);
        System.out.println("We can also have different methods in this class to perform various tasks.");
        System.out.println("For example, we can have a method to calculate the area of a rectangle.");
        int length = 10;
        int width = 5;
        int area = length * width;
        System.out.println("Area of the rectangle: " + area);
        System.out.println("We can also have a method to calculate the circumference of a circle.");
        double radius = 7.5;
        double circumference = 2 * Math.PI * radius;
        System.out.println("Circumference of the circle: " + circumference);
        System.out.println("This class can be used to demonstrate various concepts in Java programming.");
    }
}
    